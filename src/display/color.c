int	rgba_to_int(double r, double g, double b, double a)
{
	int	color;

	color = 0;
	color |= (int)(b * 255);
	color |= (int)(g * 255) << 8;
	color |= (int)(r * 255) << 16;
	color |= (int)(a * 255) << 24;
	return (color);
}
